void
checksumsTest::setUp()
{
	//Test1: short frame (tcp->on)
	tcp_packet1_checksum = 0xDDEB;

	//Test2: not multiple of 2 
	tcp_packet2_checksum = 0xACEA;

	//Test3: overflow in sum. Simulates JUMBO frame 
	tcp_packet3_checksum = 0x2C0A; 

	//Test1: short frame (udp->on)
	udp_packet1_checksum = 0x8C87;

	//Test2: not multiple of 2 
	udp_packet2_checksum = 0x1885;

	//Test3: overflow in sum. Simulates JUMBO frame 
	udp_packet3_checksum = 0x910F; 
}

void
checksumsTest::test_tcp(uint8_t* tcp_packet, unsigned int tcp_packet_checksum, unsigned int length)
{
	static int i=0;	
	i++;
	
	//Initialize the frames
	ftcpframe tcp(tcp_packet, length);

	//Do calculations
	tcp.tcp_calc_checksum( caddress(AF_INET,"172.16.0.1"), caddress(AF_INET,"172.16.0.99"), 0x06, length);

	fprintf(stderr,"Test%d TCP packet size: %d expected checksum: 0x%x, calculated :0x%x\n", i, length, tcp_packet_checksum, tcp.tcp_hdr->checksum);
//	fprintf(stderr,"Test%d TCP packet size: %d expected checksum: 0x%x, calculated :0x%x\n", i, length, ntohs(tcp_packet_checksum), ntohs(tcp.tcp_hdr->checksum));
	
	CPPUNIT_ASSERT(tcp.tcp_hdr->checksum == tcp_packet_checksum);
}

void
checksumsTest::test_udp(uint8_t* udp_packet, unsigned int udp_packet_checksum, unsigned int length)
{
	static int i=0;	
	i++;
	
	//Initialize the frames
	fudpframe udp(udp_packet, length);

	//Do calculations
	udp.udp_calc_checksum( caddress(AF_INET,"172.16.0.1"), caddress(AF_INET,"172.16.0.99"), 0x11, length);

	fprintf(stderr,"Test%d UDP packet size: %d expected checksum: 0x%x, calculated :0x%x\n", i, length,  udp_packet_checksum, udp.udp_hdr->checksum);
	fprintf(stderr,"Test%d UDP packet size: %d expected checksum: 0x%x, calculated :0x%x\n", i, length, ntohs(udp_packet_checksum), ntohs(udp.udp_hdr->checksum));
	
	CPPUNIT_ASSERT(udp.udp_hdr->checksum == udp_packet_checksum);
}
